import { GeoPoint } from "../../../../../../../../../../../subject_repositories/node-geo-point/src/geo-point";

describe('GeoPoint', () => {
  it('should calculate distance between two points correctly', () => {
    const point1 = new GeoPoint(0, 0);
    const point2 = new GeoPoint(0, 90);
    const distance = GeoPoint.calculateDistance(point1, point2);
    expect(distance).toBeGreaterThan(0);
    expect(distance).toBeLessThan(20000 * 1000);
    const point3 = new GeoPoint(90, 0);
    const distance2 = GeoPoint.calculateDistance(point1, point3);
    expect(distance2).toBeGreaterThan(0);
    expect(distance2).toBeLessThan(20000 * 1000);
    expect(distance).toBeCloseTo(distance2, 100);
    const point4 = new GeoPoint(0, -90);
    const distance3 = GeoPoint.calculateDistance(point1, point4);
    expect(distance3).toBeGreaterThan(0);
    expect(distance3).toBeLessThan(20000 * 1000);
    expect(distance).toBeCloseTo(distance3, 100);
    const point5 = new GeoPoint(-90, 0);
    const distance4 = GeoPoint.calculateDistance(point1, point5);
    expect(distance4).toBeGreaterThan(0);
    expect(distance4).toBeLessThan(20000 * 1000);
    expect(distance).toBeCloseTo(distance4, 100);
    const point6 = new GeoPoint(0, 179);
    const distance5 = GeoPoint.calculateDistance(point1, point6);
    expect(distance5).toBeGreaterThan(0);
    expect(distance5).toBeLessThan(20000 * 1000);
    const point7 = new GeoPoint(0, -179);
    const distance6 = GeoPoint.calculateDistance(point1, point7);
    expect(distance6).toBeGreaterThan(0);
    expect(distance6).toBeLessThan(20000 * 1000);
    expect(distance5).toBeCloseTo(distance6, 100);
    const point8 = new GeoPoint(0, 180);
    expect(() => GeoPoint.calculateDistance(point1, point8)).toThrow(RangeError);
    const point9 = new GeoPoint(0, -180);
    expect(() => GeoPoint.calculateDistance(point1, point9)).toThrow(RangeError);
    const point10 = new GeoPoint(90, 0);
    const distance7 = GeoPoint.calculateDistance(point1, point10);
    expect(distance7).toBeGreaterThan(0);
    expect(distance7).toBeLessThan(20000 * 1000);
    const point11 = new GeoPoint(-90, 0);
    const distance8 = GeoPoint.calculateDistance(point1, point11);
    expect(distance8).toBeGreaterThan(0);
    expect(distance8).toBeLessThan(20000 * 1000);
    expect(distance7).toBeCloseTo(distance8, 100);
    const point12 = new GeoPoint(0, 90);
    const distance9 = GeoPoint.calculateDistance(point1, point12);
    expect(distance9).toBeGreaterThan(0);
    expect(distance9).toBeLessThan(20000 * 1000);
    expect(distance7).toBeCloseTo(distance9, 100);
  });
});