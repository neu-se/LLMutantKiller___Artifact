const define = jest.fn();

describe('Complex.js', () => {
  it('should call the define function with the correct arguments', () => {
    require('./complex.js');
    expect(define).toHaveBeenCalledTimes(1);
    expect(define).toHaveBeenCalledWith([], expect.any(Function));
    const callback = define.mock.calls[0][1];
    const result = callback();
    expect(result).toHaveProperty('Complex');
    expect(result.Complex).toBeInstanceOf(Function);
    const Complex = result.Complex;
    expect(new Complex(1, 2)).toBeInstanceOf(Object);
    expect(new Complex(1, 2)).toHaveProperty('re', 1);
    expect(new Complex(1, 2)).toHaveProperty('im', 2);
    expect(new Complex(1, 2)).toHaveProperty('add');
    expect(new Complex(1, 2)).toHaveProperty('sub');
    expect(new Complex(1, 2)).toHaveProperty('mul');
    expect(new Complex(1, 2)).toHaveProperty('div');
    expect(new Complex(1, 2).add(1, 2)).toBeInstanceOf(Object);
    expect(new Complex(1, 2).sub(1, 2)).toBeInstanceOf(Object);
    expect(new Complex(1, 2).mul(1, 2)).toBeInstanceOf(Object);
    expect(new Complex(1, 2).div(1, 2)).toBeInstanceOf(Object);
    expect(new Complex(1, 2).add(1, 2)).toHaveProperty('re', 2);
    expect(new Complex(1, 2).add(1, 2)).toHaveProperty('im', 4);
    expect(new Complex(1, 2).sub(1, 2)).toHaveProperty('re', 0);
    expect(new Complex(1, 2).sub(1, 2)).toHaveProperty('im', 0);
    expect(new Complex(1, 2).mul(1, 2)).toHaveProperty('re', -3);
    expect(new Complex(1, 2).mul(1, 2)).toHaveProperty('im', 4);
    expect(new Complex(1, 2).div(1, 2)).toHaveProperty('re', 0.5);
    expect(new Complex(1, 2).div(1, 2)).toHaveProperty('im', 1);
    expect(new Complex(1, 2).add(1, 2).add(1, 2)).toBeInstanceOf(Object);
    expect(new Complex(1, 2).add(1, 2).add(1, 2)).toHaveProperty('re', 3);
    expect(new Complex(1, 2).add(1, 2).add(1, 2)).toHaveProperty('im', 6);
    expect(() => new Complex(1, 'a')).toThrowError();
    expect(() => new Complex('a', 2)).toThrowError();
    expect(new Complex(1, 2).toString()).toBe('1 + 2i');
    expect(new Complex(1, 0).toString()).toBe('1');
    expect(new Complex(0, 2).toString()).toBe('2i');
    expect(new Complex(-1, 2).toString()).toBe('-1 + 2i');
    expect(new Complex(1, -2).toString()).toBe('1 - 2i');
    expect(new Complex(0, 0).toString()).toBe('0');
    expect(new Complex(1, 2).valueOf()).toBe(1);
    expect(new Complex(0, 2).valueOf()).toBeNull();
    expect(new Complex(1, 2).clone()).toBeInstanceOf(Object);
    expect(new Complex(1, 2).clone()).toHaveProperty('re', 1);
    expect(new Complex(1, 2).clone()).toHaveProperty('im', 2);
    expect(new Complex(1, 2).equals(1, 2)).toBe(true);
    expect(new Complex(1, 2).equals(1, 3)).toBe(false);
  });
});